/**
 * Encontra criaturas fracas a um elemento especfico (110%+ damage taken)
 */
export function findCreaturesWeakTo(element: string): Creature[] {
  return ALL_CREATURES.filter(
    (creature) => creature.weakness[element] && creature.weakness[element] >= 110
  );
}

/**
 * Encontra criaturas por nvel de perigo
 */
export function findCreaturesByDanger(level: Creature["dangerLevel"]): Creature[] {
  return ALL_CREATURES.filter((creature) => creature.dangerLevel === level);
}

/**
 * Encontra criaturas por classe do bestirio
 */
export function findCreaturesByClass(bestiaryClass: string): Creature[] {
  return ALL_CREATURES.filter(
    (creature) => creature.bestiaryClass.toLowerCase() === bestiaryClass.toLowerCase()
  );
}

/**
 * Encontra criaturas com maior ratio exp/hp (melhores para hunt)
 */
export function getCreaturesByExpEfficiency(): Creature[] {
  return [...ALL_CREATURES].sort((a, b) => b.exp / b.hp - a.exp / a.hp);
}

/**
 * Encontra as melhores criaturas para um Knight de determinado level
 * Baseado em: HP/EXP ratio, fraquezas aproveitveis (physical, holy, ice)
 */
export function getRecommendedForKnight(level: number): Creature[] {
  // Rough HP thresholds por level (knight)
  const maxCreatureHp = level * 30; // pode lidar com criaturas de at 30x seu level em HP

  return ALL_CREATURES.filter((creature) => creature.hp <= maxCreatureHp)
    .sort((a, b) => {
      // Prioriza criaturas com bom exp e fraqueza a physical
      const aScore = (a.exp / a.hp) * (a.weakness.physical || 100) / 100;
      const bScore = (b.exp / b.hp) * (b.weakness.physical || 100) / 100;
      return bScore - aScore;
    })
    .slice(0, 20);
}

