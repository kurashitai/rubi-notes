export const getBossesByTier = (tier: "S" | "A" | "B" | "C"): Boss[] => {
  return bosses.filter(boss => boss.tier === tier);
};

export const getBossesByCategory = (category: string): Boss[] => {
  return bosses.filter(boss => boss.category === category);
};

export const getBossById = (id: string): Boss | undefined => {
  return bosses.find(boss => boss.id === id);
};

export const getBossesByMinLevel = (level: number): Boss[] => {
  return bosses.filter(boss => boss.recommendedLevel <= level)
    .sort((a, b) => b.recommendedLevel - a.recommendedLevel);
};

export const getDailyBosses = (): Boss[] => {
  return bosses.filter(boss => boss.cooldown === "20 horas");
};

